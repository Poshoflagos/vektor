const requirementScore = {
  None: 10,
  Low: 8,
  Medium: 5,
  High: 2,
  "Very High": 1
};

const userLevelScore = {
  None: 1,
  Low: 3,
  Medium: 6,
  High: 8,
  "Very High": 10
};

const overlapRatio = (userValues = [], pathValues = []) => {
  if (!pathValues.length) return 0.5;
  const normalizedUserValues = new Set(userValues.map((v) => String(v).toLowerCase()));
  const matches = pathValues.filter((v) => normalizedUserValues.has(String(v).toLowerCase())).length;
  return matches / pathValues.length;
};

export const calculateSkillMatch = (userProfile, path) => {
  const directSkillMatch = overlapRatio(userProfile.skills, path.requiredSkillTags);
  const technicalUser = userLevelScore[userProfile.technicalLevel] ?? 3;
  const technicalDemand = requirementScore[path.technicalRequirement] ?? 5;
  const technicalFit = Math.min(10, Math.max(0, 10 - Math.max(0, technicalDemand - technicalUser)));
  return Math.round(((directSkillMatch * 10) * 0.65 + technicalFit * 0.35) * 10) / 10;
};

export const calculateInterestMatch = (userProfile, path) => {
  return Math.round(overlapRatio(userProfile.interests, path.interestTags) * 10 * 10) / 10;
};

export const calculateCapitalFit = (userProfile, path) => {
  const userCapital = requirementScore[userProfile.capitalLevel] ?? 5;
  const pathCapitalDemand = requirementScore[path.capitalRequirement] ?? 5;
  return Math.min(10, Math.max(0, 10 - Math.max(0, pathCapitalDemand - userCapital)));
};

export const calculatePersonalityFit = (userProfile, path) => {
  return Math.round(overlapRatio(userProfile.personality, path.personalityFit) * 10 * 10) / 10;
};

export const calculateUrgencyFit = (userProfile, path) => {
  const urgency = userProfile.urgency;
  if (urgency === "Immediate") return path.speedToMoney >= 8 ? 10 : path.speedToMoney >= 6 ? 6 : 2;
  if (urgency === "30-60 days") return path.speedToMoney >= 7 ? 10 : path.speedToMoney >= 5 ? 7 : 3;
  if (urgency === "3-6 months") return path.longTermUpside >= 8 ? 9 : 6;
  return path.longTermUpside;
};

export const calculatePreferenceFit = (userProfile, path) => {
  if (!userProfile.prefers?.length) return 5;
  const pathIncomeType = path.incomeType || [];
  const pathType = [path.pathType, ...pathIncomeType].map((v) => String(v).toLowerCase());
  const preferred = userProfile.prefers.map((v) => String(v).toLowerCase());
  const hasMatch = preferred.some((pref) => pathType.some((t) => t.includes(pref)));
  return hasMatch ? 10 : 4;
};

export const calculateRiskPenalty = (userProfile, path) => {
  const baseRisk = path.regulatoryRiskLevel || 0;
  const riskTolerance = userProfile.riskTolerance || "Medium";
  const beginnerPenalty = userProfile.needsBeginnerFriendly && path.beginnerSuitability <= 3 ? 2 : 0;
  const urgencyPenalty = userProfile.urgency === "Immediate" && path.speedToMoney <= 3 ? 2 : 0;
  const riskTolerancePenalty = riskTolerance === "Low" ? baseRisk * 0.35 : riskTolerance === "Medium" ? baseRisk * 0.2 : baseRisk * 0.1;
  return Math.min(5, riskTolerancePenalty + beginnerPenalty + urgencyPenalty);
};

export const calculatePathFitScore = (userProfile, path) => {
  const skillMatch = calculateSkillMatch(userProfile, path);
  const interestMatch = calculateInterestMatch(userProfile, path);
  const beginnerFit = path.beginnerSuitability;
  const urgencyFit = calculateUrgencyFit(userProfile, path);
  const upside = path.longTermUpside;
  const marketRelevance = path.relevance2026;
  const capitalFit = calculateCapitalFit(userProfile, path);
  const personalityFit = calculatePersonalityFit(userProfile, path);
  const preferenceFit = calculatePreferenceFit(userProfile, path);
  const riskPenalty = calculateRiskPenalty(userProfile, path);

  const rawScore =
    0.22 * skillMatch +
    0.18 * interestMatch +
    0.14 * beginnerFit +
    0.14 * urgencyFit +
    0.10 * upside +
    0.08 * marketRelevance +
    0.06 * capitalFit +
    0.05 * personalityFit +
    0.03 * preferenceFit -
    riskPenalty;

  return Math.round(Math.max(0, Math.min(10, rawScore)) * 10) / 10;
};

export const rankPathsForUser = (userProfile, opportunities = AI_WEB3_OPPORTUNITIES) => {
  return opportunities
    .map((path) => ({
      ...path,
      fitScore: calculatePathFitScore(userProfile, path),
      fitBreakdown: {
        skillMatch: calculateSkillMatch(userProfile, path),
        interestMatch: calculateInterestMatch(userProfile, path),
        urgencyFit: calculateUrgencyFit(userProfile, path),
        capitalFit: calculateCapitalFit(userProfile, path),
        personalityFit: calculatePersonalityFit(userProfile, path),
        riskPenalty: calculateRiskPenalty(userProfile, path)
      }
    }))
    .sort((a, b) => b.fitScore - a.fitScore);
};

export const getRecommendationBundle = (userProfile) => {
  const ranked = rankPathsForUser(userProfile);
  const beginnerSafe = ranked
    .filter((p) => p.beginnerSuitability >= 7 && p.regulatoryRiskLevel <= 4)
    .sort((a, b) => b.fitScore - a.fitScore);

  const fastMoney = ranked
    .filter((p) => p.speedToMoney >= 7 && p.beginnerSuitability >= 6 && p.regulatoryRiskLevel <= 5)
    .sort((a, b) => b.fitScore - a.fitScore);

  const highUpside = ranked
    .filter((p) => p.longTermUpside >= 9)
    .sort((a, b) => b.fitScore - a.fitScore);

  const mastery = ranked
    .filter((p) => p.pathType === PATH_TYPES.TECHNICAL_MASTERY || p.difficulty === DIFFICULTY.ADVANCED || p.difficulty === DIFFICULTY.EXPERT)
    .sort((a, b) => b.fitScore - a.fitScore);

  return {
    bestOverallPath: ranked[0] || null,
    fastestMoneyPath: fastMoney[0] || null,
    safestBeginnerPath: beginnerSafe[0] || null,
    highestUpsidePath: highUpside[0] || null,
    longTermMasteryPath: mastery[0] || null,
    topFive: ranked.slice(0, 5)
  };
};

